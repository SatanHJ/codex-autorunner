let currentLanguage: Language = detectInitialLanguage();

function isLanguage(value: string | null | undefined): value is Language {
  return Boolean(value && SUPPORTED_LANGUAGES.includes(value as Language));
}

function readStoredLanguage(): Language | null {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    return isLanguage(stored) ? stored : null;
  } catch (_err) {
    return null;
  }
}

function detectInitialLanguage(): Language {
  const stored = readStoredLanguage();
  if (stored) return stored;
  const candidate =
    (typeof navigator !== "undefined" && navigator.language) ||
    (typeof navigator !== "undefined" && navigator.languages?.[0]) ||
    "";
  return candidate.toLowerCase().startsWith("zh") ? "zh-CN" : DEFAULT_LANGUAGE;
}

function setElementValue(element: Element, value: string, target: BindingTarget): void {
  if (target === "text") {
    element.textContent = value;
    return;
  }
  if (target === "html" && element instanceof HTMLElement) {
    element.innerHTML = value;
    return;
  }
  if (target === "title" || target === "placeholder" || target === "aria-label" || target === "content") {
    element.setAttribute(target, value);
  }
}

function syncLanguageSelects(): void {
  const selects = Array.from(
    document.querySelectorAll<HTMLSelectElement>('select[data-car-language-select]')
  );
  selects.forEach((select) => {
    if (select.dataset.i18nBound !== "1") {
      select.addEventListener("change", () => {
        void setLanguage(select.value);
      });
      select.dataset.i18nBound = "1";
    }
    select.setAttribute("aria-label", t("language.aria"));
    select.value = currentLanguage;
    const englishOption = select.querySelector<HTMLOptionElement>('option[value="en"]');
    const chineseOption = select.querySelector<HTMLOptionElement>('option[value="zh-CN"]');
    if (englishOption) englishOption.textContent = t("language.english");
    if (chineseOption) chineseOption.textContent = t("language.chinese");
  });
}

export function t(key: string): string {
  return translations[currentLanguage][key] || translations.en[key] || key;
}

export function getLanguage(): Language {
  return currentLanguage;
}

export function applyI18n(root: ParentNode = document): void {
  document.documentElement.lang = currentLanguage;
  document.title = t("meta.title");
  bindings.forEach((binding) => {
    root.querySelectorAll(binding.selector).forEach((element) => {
      setElementValue(element, t(binding.key), binding.target || "text");
    });
  });
  syncLanguageSelects();
}

export async function setLanguage(nextLanguage: string): Promise<void> {
  const normalized: Language = isLanguage(nextLanguage) ? nextLanguage : DEFAULT_LANGUAGE;
  currentLanguage = normalized;
  try {
    localStorage.setItem(STORAGE_KEY, normalized);
  } catch (_err) {
    // Ignore storage failures and keep runtime state only.
  }
  applyI18n();
  window.dispatchEvent(new CustomEvent(LANGUAGE_EVENT, { detail: { language: normalized } }));
}

export function initI18n(): void {
  currentLanguage = detectInitialLanguage();
  applyI18n();
}

export function onLanguageChange(listener: (language: Language) => void): void {
  window.addEventListener(LANGUAGE_EVENT, (event: Event) => {
    const detail = (event as CustomEvent<{ language?: Language }>).detail;
    const language = detail?.language;
    if (language && isLanguage(language)) {
      listener(language);
    }
  });
}

